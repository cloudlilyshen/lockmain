void AppTask(void *argument)
{ 
	SysMsg_t msg;
  for(;;)
  {
    if(osMessageQueueGet(appMsgHandle,&msg,NULL,osWaitForever)==osOK)
    {    	
      if (msg.src == SysMsgSrc_Uart)
      {
        AppUartMsgHandle(&msg.uart);		
      }
      else if(msg.src == SysMsgSrc_Thread)
      {
        AppThreadHandle(&msg.thread);	     	
      }
    } 
  }
}


