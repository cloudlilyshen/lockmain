//定义函数数组,用于网口处理线程回调
static const TaskHandlePtrWithThread AppThreadHandleArray[] = {
	AppThreadHandleIdle,
	AppThreadHandleSysInit,
};
#define APP_THREAD_HANDLE_COUNT_MAX      (sizeof(AppThreadHandleArray)/sizeof(AppThreadHandleArray[0]))

USER_ERROR_CODE AppThreadHandleIdle(SysMsgThread_t* msgThread)
{
  USER_ERROR_CODE errorCode = USER_ERROR_NONE;
  
  return errorCode;
}




USER_ERROR_CODE AppThreadHandleSysInit(SysMsgThread_t* msgThread)
{
  USER_ERROR_CODE errorCode = USER_ERROR_NONE;

  gprsMsg.src = SysMsgSrc_Thread;
  gprsMsg.thread.id = GPRS_MSG_Init; 
  do
  {
    osMessageQueuePut(gprsMsgHandle,&gprsMsg,NULL,osWaitForever);   
    osThreadFlagsWait(APP_FLAG_MASK_GPRS_INIT,osFlagsWaitAll,osWaitForever);  
  }while(!app.gprsConnected);    

  rs485Msg.src = SysMsgSrc_Thread;
  rs485Msg.thread.id = RS485_MSG_Init;      
  osMessageQueuePut(rs485MsgHandle,&rs485Msg,NULL,osWaitForever);           
  osThreadFlagsWait(APP_FLAG_MASK_RS485_INIT,osFlagsWaitAll,osWaitForever);
  osDelay(1000);
  //运行到此，说明硬件初始化完成
  app.AppStatus = hardwareReady;
  return errorCode;
}


void AppThreadHandle(SysMsgThread_t* msgThread)
{
  uint8_t index;
  uint8_t msgId;

  msgId = msgThread->id;

  index = msgId - APP_MSG_BASE;
	USER_ERROR_CODE errorCode;

	if(index > APP_THREAD_HANDLE_COUNT_MAX)
	{
		errorCode = USER_ID_NO_SUPPORT;
       
    return;
	}
	errorCode = AppThreadHandleArray[index](msgThread);
	// LOGA("errorCode is %x",errorCode);
	     
}



